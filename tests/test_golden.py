# 3. Unit rate verification tests
# ============================================================


class TestGoldenUnitRates:
    """Verify per-sqm unit rates match golden data.

    The Excel files contain pre-computed per-sqm rates for permit fees
    and usage fees. These are derived from shovi and the regulatory
    coefficients.
    """

    def test_permit_fee_per_sqm_main_is_constant(self):
        """Permit fee per sqm for main area is a fixed RMI rate (1486 ILS).

        The golden files all show permit_fee_per_sqm_main = 1486 regardless
        of shovi. This is a published RMI flat rate, not computed from shovi.
        """
        cases_with_rate = [c for c in GOLDEN_CASES if c.get("expected", {}).get("permit_fee_per_sqm_main") is not None]
        assert len(cases_with_rate) > 0, "No cases with permit_fee_per_sqm_main"

        values = {c["expected"]["permit_fee_per_sqm_main"] for c in cases_with_rate}
        # All golden files should show the same constant rate
        assert len(values) == 1, f"Expected a single constant permit fee rate, got {values}"
        # The constant value is 1486
        assert 1486.0 in values

    @pytest.mark.parametrize(
        "case",
        [c for c in GOLDEN_CASES if c.get("expected", {}).get("usage_fee_per_sqm_residential") is not None],
        ids=lambda c: c["case_name"],
    )
    def test_usage_fee_per_sqm_residential(self, case):
        """Usage fee per sqm for residential matches golden data.

        Formula: shovi * usage_rate(0.05) * main_coeff(1.0)
        """
        inp = case["inputs"]
        exp = case["expected"]

        shovi = inp["shovi_per_sqm"]
        usage_rate = CONFIG["usage_fee_residential"]["value"]

        expected = shovi * usage_rate
        golden_value = exp["usage_fee_per_sqm_residential"]

        assert_within_tolerance(
            expected,
            golden_value,
            label=f"{case['case_name']} usage fee/sqm residential",
        )


