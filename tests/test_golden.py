"""Golden test suite -- verify calculations against 24 real example reports.

Each test case is extracted from actual client reports with known-correct values.
Tests verify our calc engine produces matching results within 1% tolerance.

The golden data file (golden_cases.json) is generated by
tests/golden/extract_golden_data.py from the 25 example Excel files.
"""

from __future__ import annotations

import json
from pathlib import Path

import pytest

GOLDEN_DATA_PATH = Path(__file__).parent / "golden" / "golden_cases.json"


def load_golden_cases() -> list[dict]:
    """Load golden test cases from JSON."""
    if not GOLDEN_DATA_PATH.exists():
        pytest.skip("Golden data not yet extracted -- run extract_golden_data.py first")
    with open(GOLDEN_DATA_PATH, encoding="utf-8") as f:
        return json.load(f)


def _load_config() -> dict:
    config_path = Path(__file__).parent.parent / "src" / "config" / "rates_config.json"
    with open(config_path, encoding="utf-8") as f:
        return json.load(f)


CONFIG = _load_config()

try:
    GOLDEN_CASES = load_golden_cases()
except Exception:
    GOLDEN_CASES = []


