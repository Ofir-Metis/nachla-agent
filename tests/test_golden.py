# 1. Hivun 3.75% tests
# ============================================================


class TestGoldenHivun375:
    """Verify 3.75% capitalization against golden data.

    The golden expected value is the GROSS amount:
        sqm_375 * shovi * 0.0375
    which equals calculate_hivun_375(...).gross_hivun
    """

    @pytest.mark.parametrize(
        "case",
        [c for c in GOLDEN_CASES if c.get("expected", {}).get("hivun_375_gross") is not None],
        ids=lambda c: c["case_name"],
    )
    def test_hivun_375_gross_within_tolerance(self, case):
        """Hivun 3.75% gross amount matches golden data within 1%."""
        inp = case["inputs"]
        exp = case["expected"]

        sqm_375 = exp.get("sqm_375", 806.25)
        shovi = inp["shovi_per_sqm"]
        priority = inp.get("priority_area")

        result = calculate_hivun_375(
            sqm_equivalent_375=sqm_375,
            shovi_per_sqm=shovi,
            priority_area=priority,
            development_costs=0.0,  # Golden gross is before dev costs
        )

        assert "error" not in result, f"Calculation error: {result.get('error')}"
        assert_within_tolerance(
            result["gross_hivun"],
            exp["hivun_375_gross"],
            label=f"{case['case_name']} hivun 3.75% gross",
        )

    @pytest.mark.parametrize(
        "case",
        [c for c in GOLDEN_CASES if c.get("expected", {}).get("hivun_375_net") is not None],
        ids=lambda c: c["case_name"],
    )
    def test_hivun_375_net_within_tolerance(self, case):
        """Hivun 3.75% net (after dev costs) matches golden data within 1%.

        The golden files use a fixed development cost deduction of 6562.5
        in the 3.75% track (column G), which is a standard per-nachla value
        independent of the sqm-sheet development costs.
        """
        inp = case["inputs"]
        exp = case["expected"]

        sqm_375 = exp.get("sqm_375", 806.25)
        shovi = inp["shovi_per_sqm"]
        priority = inp.get("priority_area")

        # The golden files use a fixed deduction of 6562.5 for the 3.75% track
        golden_dev_cost = 6562.5

        result = calculate_hivun_375(
            sqm_equivalent_375=sqm_375,
            shovi_per_sqm=shovi,
            priority_area=priority,
            development_costs=golden_dev_cost,
        )

        assert "error" not in result, f"Calculation error: {result.get('error')}"
        assert_within_tolerance(
            result["net_after_dev_costs"],
            exp["hivun_375_net"],
            label=f"{case['case_name']} hivun 3.75% net",
        )


