# 4. Split (pitzul) gross tests
# ============================================================


class TestGoldenSplit:
    """Verify split cost gross calculations against golden data.

    The split in the golden data is divided into:
    - split_discounted_gross: first 245/207.5 sqm at 29.25% rate
    - split_undiscounted_gross: remainder at 33% rate
    """

    @pytest.mark.parametrize(
        "case",
        [c for c in GOLDEN_CASES if c.get("expected", {}).get("split_total_gross") is not None],
        ids=lambda c: c["case_name"],
    )
    def test_split_total_gross_within_tolerance(self, case):
        """Total split cost (gross) matches golden data within 1%."""
        exp = case["expected"]

        split_total = exp["split_total_gross"]

        # Verify the sum of discounted + undiscounted parts
        discounted = exp.get("split_discounted_gross", 0) or 0
        undiscounted = exp.get("split_undiscounted_gross", 0) or 0
        computed_total = discounted + undiscounted

        if split_total and split_total > 0:
            assert_within_tolerance(
                computed_total,
                split_total,
                label=f"{case['case_name']} split total = discounted + undiscounted",
            )

    @pytest.mark.parametrize(
        "case",
        [
            c
            for c in GOLDEN_CASES
            if c.get("expected", {}).get("split_discounted_gross") is not None
            and c.get("expected", {}).get("sqm_split_discounted") is not None
        ],
        ids=lambda c: c["case_name"],
    )
    def test_split_discounted_formula(self, case):
        """Split discounted portion follows the formula: sqm * 0.2925 * shovi."""
        inp = case["inputs"]
        exp = case["expected"]

        sqm_discounted = exp["sqm_split_discounted"]
        shovi = inp["shovi_per_sqm"]

        # The discounted split rate is 29.25% (from priority area config)
        # For standard (no priority) areas: rate = 0.2925
        expected = sqm_discounted * 0.2925 * shovi
        golden = exp["split_discounted_gross"]

        assert_within_tolerance(
            expected,
            golden,
            label=f"{case['case_name']} split discounted formula",
        )


