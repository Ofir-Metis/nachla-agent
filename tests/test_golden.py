# ============================================================
# Helper: tolerance assertion
# ============================================================


def assert_within_tolerance(actual: float, expected: float, tolerance: float = 0.01, label: str = ""):
    """Assert actual is within tolerance of expected.

    For expected == 0, use absolute tolerance of 100 ILS.
    """
    if expected == 0:
        assert abs(actual) < 100, f"{label}: expected ~0 but got {actual}"
    else:
        rel_diff = abs(actual - expected) / abs(expected)
        assert rel_diff < tolerance, (
            f"{label}: expected {expected}, got {actual} (diff={rel_diff:.4%}, tolerance={tolerance:.2%})"
        )


