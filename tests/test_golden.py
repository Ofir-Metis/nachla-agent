# 7. Cross-validation: hivun 375 gross = sqm_375 * shovi * rate
# ============================================================


class TestGoldenCrossValidation:
    """Cross-validate extracted values for internal consistency."""

    @pytest.mark.parametrize(
        "case",
        [
            c
            for c in GOLDEN_CASES
            if c.get("expected", {}).get("hivun_375_gross") is not None
            and c.get("expected", {}).get("sqm_375") is not None
        ],
        ids=lambda c: c["case_name"],
    )
    def test_hivun_375_formula_consistent(self, case):
        """Verify: hivun_375_gross == sqm_375 * shovi * 0.0375."""
        inp = case["inputs"]
        exp = case["expected"]

        computed = exp["sqm_375"] * inp["shovi_per_sqm"] * 0.0375
        assert_within_tolerance(
            computed,
            exp["hivun_375_gross"],
            label=f"{case['case_name']} hivun 375 formula cross-check",
        )

    @pytest.mark.parametrize(
        "case",
        [
            c
            for c in GOLDEN_CASES
            if (
                c.get("expected", {}).get("purchase_33_gross") is not None
                or c.get("expected", {}).get("purchase_20_gross") is not None
            )
            and c.get("expected", {}).get("sqm_nachla_25dunam") is not None
        ],
        ids=lambda c: c["case_name"],
    )
    def test_purchase_formula_consistent(self, case):
        """Verify: purchase_gross == total_sqm * shovi * rate.

        The rate is typically 0.33 but may be 0.2 for high-value areas.
        Uses the exact total_sqm from the Excel (column B of the purchase row)
        when available, since some cases compute it differently.
        """
        inp = case["inputs"]
        exp = case["expected"]

        # Use the actual total_sqm from the Excel if available
        if exp.get("purchase_total_sqm") is not None:
            total_sqm = exp["purchase_total_sqm"]
        else:
            sqm_nachla = exp["sqm_nachla_25dunam"]
            sqm_potential = exp.get("sqm_potential", 0) or 0
            prior_permits = inp.get("prior_permit_fees_sqm", 0) or 0
            total_sqm = sqm_nachla + sqm_potential - prior_permits

        # Determine which purchase track is used
        if exp.get("purchase_33_gross") is not None:
            golden_gross = exp["purchase_33_gross"]
            rate = exp.get("purchase_rate", 0.33)
        else:
            golden_gross = exp["purchase_20_gross"]
            rate = exp.get("purchase_rate", 0.2)

        computed = total_sqm * inp["shovi_per_sqm"] * rate

        assert_within_tolerance(
            computed,
            golden_gross,
            label=f"{case['case_name']} purchase formula cross-check (rate={rate})",
        )
